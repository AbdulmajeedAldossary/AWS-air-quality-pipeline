CREATE TABLE unified_air_quality (
         date DATE,
         CO DOUBLE PRECISION,
         SO2 DOUBLE PRECISION,
         NO2 DOUBLE PRECISION,
         O3 DOUBLE PRECISION,
         PM10 DOUBLE PRECISION,
         NO DOUBLE PRECISION,
         PM2_5 DOUBLE PRECISION,
         NH3 DOUBLE PRECISION,
         station VARCHAR(50),
         longitude DOUBLE PRECISION,
         latitude DOUBLE PRECISION
     );